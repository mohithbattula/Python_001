#creating a set 
set={5,10,15,20,25}
print(set)

#adding elements into a set
set.add(30)
print(set)

#removing element from thae set
set.remove(5)
print(set)

#updating the elements in a set
set.discard(10)
print(set)

#remove () will raise an error if the element is not present in the set, while discard () will not.